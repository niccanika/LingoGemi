class MainActivity : ComponentActivity() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge(
            statusBarStyle = SystemBarStyle.auto(
                android.graphics.Color.TRANSPARENT,
                android.graphics.Color.TRANSPARENT
            )
        )

        setContent {
            LingoGemiTheme {

                // A surface container using the 'background' color from the theme
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background
                ) {

                    val navController = rememberNavController()

                    NavHost(navController = navController, startDestination = Screen.Home.route ) {
                        composable(Screen.Home.route) {
                            HomeScreen(navController = navController)
                        }

                        composable(
                            Screen.Chat.route + "/{chosenLanguage}",
                            arguments = listOf(
                                navArgument(name = "chosenLanguage") {type = NavType.StringType}
                            )
                            ) { backStackEntry ->

                            val chosenLanguage = backStackEntry.arguments?.getString("chosenLanguage")

                            if (chosenLanguage != null) {
                                ChatBot(navController = navController, chosenLanguage = chosenLanguage)
                            }
                        }

                        composable(Screen.Lang.route) {
                            ChooseLanguage(navController = navController)
                        }
                    }


                }
            }
        }
    }

}
